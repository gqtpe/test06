<script lang="ts" setup>
import {useAuth} from "@/features/Auth/authStore.ts";
import {onBeforeMount, ref} from "vue";
import {useRoute, useRouter} from "vue-router";

onBeforeMount(() => {
  if (useAuth().isLoggedIn()) {
    router.push("/")
  }
})
const {logIn} = useAuth()
const key = ref()
const route = useRoute()
const router = useRouter()
const redirectPage = route.query.redirect as string | undefined
const submit = async (e: Event) => {
  e.preventDefault();
  const response = await logIn(key.value)
  if (response) {
    router.push(redirectPage || '/')
  }

}
</script>

<template>
  <div class="flex min-h-full flex-col justify-center px-6 py-12 lg:px-8">
    <div class="sm:mx-auto sm:w-full sm:max-w-sm">
      <h2 class="mt-10 text-center text-2xl/9 font-bold tracking-tight text-white">Please enter Auth Key</h2>
    </div>

    <div class="mt-10 sm:mx-auto sm:w-full sm:max-w-sm">
      <form action="#" method="POST" class="space-y-6">
        <div>
          <label for="KEY" class="block text-sm/6 font-medium text-gray-100">Key</label>
          <div class="mt-2">
            <input
                id="KEY"
                :value="key"
                @input="event => key = event.target?.value!"
                type="text"
                name="key"
                class="block w-full rounded-md bg-white/5 px-3 py-1.5 text-base text-white outline-1 -outline-offset-1 outline-white/10 placeholder:text-gray-500 focus:outline-2 focus:-outline-offset-2 focus:outline-indigo-500 sm:text-sm/6"/>
          </div>
        </div>
        <div>
          <button type="submit" @click="submit"
                  class="flex w-full justify-center rounded-md bg-indigo-500 px-3 py-1.5 text-sm/6 font-semibold text-white hover:bg-indigo-400 focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-500">
            Submit
          </button>
        </div>
      </form>
    </div>
  </div>
</template>